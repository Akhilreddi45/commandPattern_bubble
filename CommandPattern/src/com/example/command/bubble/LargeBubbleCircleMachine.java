package com.example.command.bubble;
/**
* Large Bubble Circle Machine
*/
public class LargeBubbleCircleMachine {

	public LargeBubbleCircleMachine() {

	}
	/**
	* start machine.
	*/
	public void start() {
		System.out.println("LargeBubbleCircleMachine Started");
	}
	/**
	* blow machine.
	*/
	public void blow() {
		System.out.println("LargeBubbleCircleMachine Blowing Circles");

	}
	/**
	* stop machine.
	*/
	public void stop() {
		System.out.println("LargeBubbleCircleMachine Stopped");

	}

}
